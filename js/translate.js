// Variable de estado de idioma expuesta globalmente
window.currentLanguage = localStorage.getItem("selectedLanguage") || "es";

function getNestedTranslation(obj, path) {
  return path
    .split(".")
    .reduce((prev, curr) => (prev ? prev[curr] : null), obj);
}

function setLanguage(lang) {
  window.currentLanguage = lang;
  localStorage.setItem("selectedLanguage", lang);
  document.documentElement.lang = lang;

  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const translation = getNestedTranslation(translations[lang], key);

    if (translation) {
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translation;
      } else {
        el.textContent = translation;
      }
    }
  });

  const langLabel = document.getElementById("lang-label");
  if (langLabel) {
    langLabel.textContent = lang === "es" ? "EN" : "ES";
  }
}

function toggleLanguage() {
  const nextLang = window.currentLanguage === "es" ? "en" : "es";
  setLanguage(nextLang);
}

document.addEventListener("DOMContentLoaded", () => {
  const btnLanguage = document.getElementById("btn-language");
  if (btnLanguage) {
    btnLanguage.addEventListener("click", toggleLanguage);
  }
  setLanguage(window.currentLanguage);
});
